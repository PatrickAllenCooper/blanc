"""
Convert Tier 1 instances (instances/tier1/<domain>/instances.json) into the
{domain}_dev_instances.json layout expected by experiments/run_evaluation.py
_load_level2_instances().

Tier 1 instances were generated by an OpenCyc + ConceptNet pipeline that
ablates one defeasible rule from a source theory. The on-disk records keep
the abductive task spec (target, candidates, gold) but do not embed the
source theory, so a literal evaluation would render an empty theory in the
prompt.

This converter reconstructs a *minimal grounded theory* per instance: parse
the gold rule's body atoms and substitute the target's first argument into
each body atom variable to produce ground facts. This is the smallest
theory that makes the gold rule fire and derive the target when added.

The reconstructed theory is informationally weaker than Tier 0 (no
distractor rules, no taxonomic context), so this protocol should be read
as a *coverage* / generalization probe rather than a difficulty match for
Tier 0. The §6 paragraph that consumes these results frames it accordingly.

Output schema matches what _load_level2_instances() expects:
{
  "instances": [
    {
      "target": "...",
      "level": 2,
      "candidates": [...],
      "gold": [...],
      "metadata": {
        "facts": [<ground-fact strings>],
        "rules": [],     # empty: gold IS the missing rule
        "instance_id": "tier1-<domain>-<idx>",
        ... original metadata
      }
    },
    ...
  ]
}
"""

from __future__ import annotations

import json
import re
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]

DOMAINS = ["biology", "chemistry", "everyday", "legal", "materials"]
LIMIT_PER_DOMAIN = 30


def _parse_atom(atom: str) -> tuple[str, list[str]] | None:
    """Parse 'pred(arg1, arg2, ...)' into (pred, [arg1, arg2, ...])."""
    atom = atom.strip()
    m = re.match(r"^([a-zA-Z_][\w]*)\(([^)]*)\)$", atom)
    if not m:
        return None
    pred = m.group(1)
    args = [a.strip() for a in m.group(2).split(",") if a.strip()]
    return pred, args


def _parse_rule(rule_str: str) -> dict | None:
    """Parse 'label: body => head' into {label, body, head, rule_type}."""
    s = rule_str.strip()
    if ":" in s:
        label, rest = s.split(":", 1)
        label = label.strip()
        rest = rest.strip()
    else:
        label, rest = "", s

    if "~>" in rest:
        body_part, head_part = rest.split("~>", 1)
        rule_type = "DEFEATER"
    elif "=>" in rest:
        body_part, head_part = rest.split("=>", 1)
        rule_type = "DEFEASIBLE"
    elif ":-" in rest:
        body_part, head_part = rest.split(":-", 1)
        rule_type = "STRICT"
    else:
        return None

    body_atoms = [a.strip() for a in body_part.split(",") if a.strip()]
    head = head_part.strip()
    return {"label": label, "body": body_atoms, "head": head, "rule_type": rule_type}


def _ground_fact(body_atom: str, subject: str) -> str | None:
    """Substitute the variable in a body atom with the given subject constant.

    Body atoms in Tier 1 are universally quantified over a single variable X.
    Returns 'pred(subject)' if the atom has form 'pred(X)' (any single-letter
    variable), otherwise returns the atom verbatim if it already looks ground.
    """
    parsed = _parse_atom(body_atom)
    if parsed is None:
        return None
    pred, args = parsed
    if not args:
        return None
    # Replace single-letter variables with the subject; keep other args verbatim.
    new_args = []
    for a in args:
        if len(a) == 1 and a.isupper():
            new_args.append(subject)
        else:
            new_args.append(a)
    return f"{pred}({', '.join(new_args)})"


def _build_minimal_theory(item: dict) -> tuple[list[str], list[str]]:
    """Return (facts, rules) for the minimal theory needed to make the gold
    rule fire and derive the target when added.

    Strategy: parse the gold rule, take its body atoms, ground each body
    variable to the target's first argument. The resulting facts become the
    visible theory; rules stays empty (the gold rule is the missing piece).
    """
    target = item.get("target", "").strip()
    target_parsed = _parse_atom(target)
    if target_parsed is None:
        return [], []
    _, target_args = target_parsed
    if not target_args:
        return [], []
    subject = target_args[0]

    gold_strs = item.get("gold", [])
    if not gold_strs:
        return [], []
    gold = _parse_rule(gold_strs[0])
    if gold is None:
        return [], []

    facts: list[str] = []
    for body_atom in gold["body"]:
        gf = _ground_fact(body_atom, subject)
        if gf is not None:
            facts.append(gf)

    return facts, []


def _convert_instance(item: dict, domain: str, idx: int) -> dict | None:
    if item.get("level") != 2:
        return None
    facts, rules = _build_minimal_theory(item)
    if not facts:
        return None
    return {
        "target": item.get("target", ""),
        "level": 2,
        "candidates": item.get("candidates", []),
        "gold": item.get("gold", []),
        "metadata": {
            "facts": facts,
            "rules": rules,
            "instance_id": f"tier1-{domain}-l2-{idx:04d}",
            "original_metadata": item.get("metadata", {}),
            "original_theory_size": item.get("theory_size"),
            "tier": 1,
        },
    }


def main() -> int:
    out_dir = ROOT / "instances" / "tier1_eval"
    out_dir.mkdir(parents=True, exist_ok=True)

    summary = {}
    for domain in DOMAINS:
        src = ROOT / "instances" / "tier1" / domain / "instances.json"
        if not src.exists():
            print(f"  {domain}: source missing at {src}")
            continue
        with open(src) as f:
            data = json.load(f)
        items = data if isinstance(data, list) else data.get("instances", [])

        l2 = [it for it in items if it.get("level") == 2]
        converted = []
        skipped = 0
        for idx, it in enumerate(l2):
            if len(converted) >= LIMIT_PER_DOMAIN:
                break
            rec = _convert_instance(it, domain, idx)
            if rec is None:
                skipped += 1
                continue
            converted.append(rec)

        out_path = out_dir / f"{domain}_dev_instances.json"
        with open(out_path, "w") as f:
            json.dump({"metadata": {"domain": domain, "tier": 1, "level": 2,
                                    "total_l2_in_source": len(l2),
                                    "converted": len(converted),
                                    "skipped": skipped},
                       "instances": converted}, f, indent=2)
        summary[domain] = (len(converted), skipped, len(l2))
        print(f"  {domain:<12} converted={len(converted):>3}  skipped={skipped:>2}  source_l2={len(l2):>3}")

    total = sum(c for c, _, _ in summary.values())
    print(f"\nTotal Tier 1 L2 instances ready for evaluation: {total}")
    print(f"Output directory: {out_dir}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
