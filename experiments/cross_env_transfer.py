"""
E5: Cross-environment transfer evaluation.

Evaluates foundation models on all five DeFAb domain sets simultaneously:
    - sc2live (live SC2 grounding)
    - rts_engagement (hand-authored SC2 ROE)
    - lux_ai_s3 (Lux AI S3)
    - biology / legal / materials (knowledge-base domains)

This script directly tests Hypothesis H5 (paper.tex line 21):
    If models fine-tuned on sc2live conflicts score comparably on RTS,
    Lux AI, biology, and legal L3 instances, this demonstrates that
    DeFAb measures structural defeasible reasoning rather than domain-
    specific vocabulary knowledge.

Usage::

    # Compare all environments, mock provider
    python experiments/cross_env_transfer.py --provider mock --instance-limit 20

    # Full Foundry evaluation
    python experiments/cross_env_transfer.py \\
        --provider foundry-deepseek \\
        --modalities M4 \\
        --strategies direct cot \\
        --include-level3

    # Point to a fine-tuned checkpoint (from E3 sc2live DPO)
    python experiments/cross_env_transfer.py \\
        --provider curc \\
        --model path/to/finetuned-checkpoint \\
        --curc-base-url http://localhost:8000 \\
        --include-level3

Output: experiments/results/cross_env_transfer_{provider}_{timestamp}.json

Key table format:
    Environment     L2 Acc    L3 Acc    L3 Graded
    ------------------------------------------------
    sc2live           --        --        --
    rts_engagement    --        --        --
    lux_ai_s3         --        --        --
    biology           --        --        --
    legal             --        --        --
    materials         --        --        --

Author: Anonymous Authors
"""

from __future__ import annotations

import argparse
import json
import os
import sys
from datetime import datetime
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT / "src"))
sys.path.insert(0, str(ROOT / "experiments"))

try:
    from dotenv import load_dotenv
    load_dotenv(ROOT / ".env")
except ImportError:
    pass


# ---------------------------------------------------------------------------
# Environment registry
# ---------------------------------------------------------------------------

ENVIRONMENTS = {
    "sc2live": {
        "name": "SC2 Live",
        "instances_path": ROOT / "instances" / "sc2live_instances.json",
        "loader": "json_file",
    },
    "rts_engagement": {
        "name": "RTS Engagement (hand-authored)",
        "instances_path": ROOT / "instances" / "rts_engagement_instances.json",
        "loader": "json_file",
    },
    "lux_ai_s3": {
        "name": "Lux AI S3",
        "instances_path": ROOT / "instances" / "lux_engagement_instances.json",
        "loader": "json_file",
    },
}

# Standard domain instance paths (generated by generate_multitier_instances.py)
_DOMAIN_PATTERNS = {
    "biology":   ["biology_instances.json", "biology_*.json"],
    "legal":     ["legal_instances.json", "legal_*.json"],
    "materials": ["materials_instances.json", "materials_*.json"],
}


def _find_domain_instances(domain: str) -> Path | None:
    instances_dir = ROOT / "instances"
    for pattern in _DOMAIN_PATTERNS.get(domain, []):
        candidates = sorted(instances_dir.glob(pattern))
        if candidates:
            return candidates[0]
    return None


def _load_instances_from_json(path: Path, level_filter: list[int], max_n: int):
    """Generic JSON instance loader (shared format across all domains)."""
    if not path.exists():
        return []
    try:
        from blanc.author.generation import AbductiveInstance
        from blanc.core.theory import Theory, Rule, RuleType

        with open(path) as f:
            data = json.load(f)
        raw = data.get("instances", data if isinstance(data, list) else [])
        instances = []
        for i, item in enumerate(raw[:max_n]):
            level = item.get("level", 2)
            if level_filter and level not in level_filter:
                continue
            meta = item.get("metadata", {})
            theory = Theory()
            for fact in meta.get("facts", []):
                theory.add_fact(fact)
            inst = AbductiveInstance(
                D_minus=theory,
                target=item.get("target", ""),
                candidates=item.get("candidates", []),
                gold=item.get("gold", []),
                level=level,
                metadata={"domain": item.get("domain", path.stem), **meta},
            )
            inst.id = item.get("scenario_id", f"{path.stem}-{i:04d}")
            instances.append(inst)
        return instances
    except Exception as exc:
        print(f"  WARNING: could not load {path}: {exc}")
        return []


def load_all_environments(
    level_filter: list[int],
    max_n: int,
    include_domains: list[str] | None = None,
) -> dict[str, list]:
    """Return {env_key: [AbductiveInstance, ...]} for all available environments."""
    result: dict[str, list] = {}

    # Game environments
    for key, cfg in ENVIRONMENTS.items():
        insts = _load_instances_from_json(cfg["instances_path"], level_filter, max_n)
        if insts:
            result[key] = insts
            print(f"  {key:<20} {len(insts):>5} instances")
        else:
            print(f"  {key:<20} (not available -- run generation script first)")

    # Knowledge-base domains
    domains = include_domains or list(_DOMAIN_PATTERNS.keys())
    for domain in domains:
        path = _find_domain_instances(domain)
        if path:
            insts = _load_instances_from_json(path, level_filter, max_n)
            if insts:
                result[domain] = insts
                print(f"  {domain:<20} {len(insts):>5} instances")
        else:
            print(f"  {domain:<20} (not available)")

    return result


def evaluate_one_environment(
    env_key: str,
    instances: list,
    model_iface,
    modalities: list[str],
    strategies: list[str],
    verbose: bool,
) -> dict:
    """Run evaluation for a single environment. Returns a summary dict."""
    try:
        from evaluation_pipeline import EvaluationPipeline
    except ImportError:
        return {
            "env": env_key,
            "n": len(instances),
            "level2_accuracy": None,
            "level3_accuracy": None,
            "note": "evaluation_pipeline not available",
        }

    model_name = getattr(model_iface, "model_name", "model") or "model"
    models_dict = {model_name: model_iface}

    pipeline = EvaluationPipeline(
        instances=instances,
        models=models_dict,
        modalities=modalities,
        strategies=strategies,
    )
    results = pipeline.run()
    summary = getattr(results, "summary", {}) if results else {}
    if isinstance(results, dict):
        summary = results.get("summary", {})
    return {
        "env": env_key,
        "n": len(instances),
        "level2_accuracy": summary.get("level2_accuracy"),
        "level3_accuracy": summary.get("level3_accuracy"),
        "level3_graded": summary.get("level3_graded"),
    }


def main() -> int:
    parser = argparse.ArgumentParser(
        description="E5 cross-environment transfer evaluation"
    )
    parser.add_argument("--provider", default="mock")
    parser.add_argument("--model", default=None)
    parser.add_argument("--curc-base-url", default=None)
    parser.add_argument("--modalities", nargs="+", default=["M4"])
    parser.add_argument("--strategies", nargs="+", default=["direct", "cot"])
    parser.add_argument("--include-level3", action="store_true")
    parser.add_argument("--instance-limit", type=int, default=100)
    parser.add_argument("--domains", nargs="+",
                        default=["biology", "legal", "materials"],
                        help="Knowledge-base domains to include")
    parser.add_argument("--output-dir", default="experiments/results/")
    parser.add_argument("--verbose", action="store_true")
    args = parser.parse_args()

    print("=" * 70)
    print("E5: CROSS-ENVIRONMENT TRANSFER EVALUATION")
    print("=" * 70)
    print(f"Provider    : {args.provider}")
    print(f"Modalities  : {args.modalities}")
    print(f"Strategies  : {args.strategies}")
    print(f"Date        : {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    print()

    level_filter = [1, 2, 3] if args.include_level3 else [1, 2]

    print("Loading environments:")
    all_envs = load_all_environments(
        level_filter=level_filter,
        max_n=args.instance_limit,
        include_domains=args.domains,
    )
    print(f"\nTotal environments loaded: {len(all_envs)}")

    if not all_envs:
        print("No instances available. Generate traces and instances first.")
        return 1

    try:
        import os as _os
        from model_interface import create_model_interface
        _api_key = None
        if args.provider.startswith("foundry-"):
            _api_key = _os.environ.get("FOUNDRY_API_KEY") or None
        model_iface = create_model_interface(
            provider=args.provider,
            api_key=_api_key,
            model=args.model,
        )
    except ImportError:
        print("WARNING: model_interface not available; printing instance counts only")
        model_iface = None

    all_summaries: list[dict] = []

    print("\nRunning evaluations:")
    print("-" * 70)

    for env_key, instances in all_envs.items():
        print(f"\n{env_key} ({len(instances)} instances)...")
        if model_iface:
            summary = evaluate_one_environment(
                env_key, instances, model_iface,
                modalities=args.modalities,
                strategies=args.strategies,
                verbose=args.verbose,
            )
        else:
            summary = {"env": env_key, "n": len(instances),
                       "level2_accuracy": None, "level3_accuracy": None,
                       "note": "no model interface"}
        all_summaries.append(summary)
        l2 = summary.get("level2_accuracy")
        l3 = summary.get("level3_accuracy")
        print(f"  L2: {l2!s:>8}  L3: {l3!s:>8}")

    # Print comparison table
    print("\n" + "=" * 70)
    print(f"{'Environment':<22} {'L2 Acc':>8} {'L3 Acc':>8} {'L3 Graded':>10}")
    print("-" * 70)
    for s in all_summaries:
        l2 = f"{s['level2_accuracy']:.1%}" if isinstance(s.get("level2_accuracy"), float) else "--"
        l3 = f"{s['level3_accuracy']:.1%}" if isinstance(s.get("level3_accuracy"), float) else "--"
        l3g = f"{s['level3_graded']:.2f}" if isinstance(s.get("level3_graded"), float) else "--"
        print(f"  {s['env']:<20} {l2:>8} {l3:>8} {l3g:>10}")

    # Save results
    output_dir = ROOT / args.output_dir
    output_dir.mkdir(parents=True, exist_ok=True)
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    out_path = output_dir / f"cross_env_transfer_{args.provider}_{timestamp}.json"
    with open(out_path, "w") as f:
        json.dump({
            "metadata": {
                "provider": args.provider,
                "modalities": args.modalities,
                "strategies": args.strategies,
                "level_filter": level_filter,
                "date": datetime.now().isoformat(),
                "note": "E5 cross-environment transfer test (paper.tex H5)",
            },
            "results": all_summaries,
        }, f, indent=2)
    print(f"\nResults saved to: {out_path}")

    print("\n" + "=" * 70)
    print("E5 CROSS-ENVIRONMENT TRANSFER EVALUATION COMPLETE")
    print("=" * 70)
    return 0


if __name__ == "__main__":
    sys.exit(main())
