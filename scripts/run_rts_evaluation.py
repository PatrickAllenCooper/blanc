"""
CLI entry point: evaluate foundation models on DeFAb-ROE instances.

This script runs the same evaluation protocol as the main DeFAb benchmark
(experiments/run_evaluation.py) on the RTS engagement knowledge base,
allowing direct comparison of model performance on ROE exception reasoning
versus the existing biology/legal/materials domains.

Usage examples:

    # Mock dry-run (no API key needed)
    python scripts/run_rts_evaluation.py --provider mock --instance-limit 6

    # Azure / CURC vLLM
    python scripts/run_rts_evaluation.py \\
        --provider curc \\
        --model Qwen/Qwen2.5-72B-Instruct-AWQ \\
        --curc-base-url http://localhost:8000 \\
        --modalities M4 M2 \\
        --strategies direct cot \\
        --include-level3

    # Foundry (GPT-5.2, Claude, Kimi, DeepSeek)
    python scripts/run_rts_evaluation.py \\
        --provider foundry-deepseek \\
        --modalities M4 \\
        --strategies direct cot \\
        --include-level3

The script:
  1. Generates RTS engagement instances (or loads from a pre-generated file).
  2. Adapts them to the AbductiveInstance format expected by EvaluationPipeline.
  3. Runs evaluation with the specified model(s).
  4. Saves results to experiments/results/rts_{provider}_{timestamp}.json.
  5. Prints a cross-domain comparison table if existing domain results are found.

Hypotheses being tested (from the plan):
  H1: L2 ROE accuracy ~ legal domain L2 accuracy (normative framework).
  H2: L3 reasoning models > instruction models under CoT.
  H3: ROE L3 accuracy may exceed biology L3 (explicit exception structure).
  H4: E4 (non-conservative defeaters) more frequent in ROE than biology.

Author: Patrick Cooper
"""

