# ── Instance loading helpers ──────────────────────────────────────────────────

def _build_theory_from_record(item: dict) -> Theory:
    """Reconstruct a placeholder Theory from a serialized instance record."""
    meta = item.get("metadata", {})
    facts = meta.get("facts", [])
    rules_raw = meta.get("rules", [])
    rules = []
    for r in rules_raw:
        if isinstance(r, dict):
            try:
                rules.append(Rule(
                    head=r.get("head", ""),
                    body=tuple(r.get("body", [])),
                    rule_type=RuleType(r.get("rule_type", "defeasible")),
                    label=r.get("label"),
                ))
            except (ValueError, TypeError):
                continue
    return Theory(facts=set(facts), rules=rules, superiority={})


def _reconstruct_theory_from_l3(item: dict) -> Theory:
    """Reconstruct a Theory from theory_facts / theory_rules strings."""
    facts = list(item.get("theory_facts", []))
    rules: list[Rule] = []
    for rule_str in item.get("theory_rules", []):
        if ":" in rule_str:
            label, rest = rule_str.split(":", 1)
            label, rest = label.strip(), rest.strip()
        else:
            label, rest = None, rule_str.strip()

        if "~>" in rest:
            body_part, head_part = rest.split("~>", 1)
            rtype = RuleType.DEFEATER
        elif "=>" in rest:
            body_part, head_part = rest.split("=>", 1)
            rtype = RuleType.DEFEASIBLE
        elif ":-" in rest:
            body_part, head_part = rest.split(":-", 1)
            rtype = RuleType.STRICT
        else:
            rules.append(Rule(head=rest.strip(), body=(), rule_type=RuleType.STRICT, label=label))
            continue

        body_atoms = tuple(b.strip() for b in body_part.split(",") if b.strip())
        rules.append(Rule(head=head_part.strip(), body=body_atoms, rule_type=rtype, label=label))

    return Theory(facts=set(facts), rules=rules, superiority={})


def load_rts_instances(
    instances_path: Path,
    limit: int = 50,
    include_level3: bool = True,
    level3_limit: int = 6,
) -> list[AbductiveInstance]:
    """
    Load RTS engagement instances from a JSON file generated by
    scripts/generate_rts_instances.py.
    """
    if not instances_path.exists():
        print(f"  Warning: {instances_path} not found.")
        print("  Run: python scripts/generate_rts_instances.py "
              f"--output {instances_path}")
        return []

    with open(instances_path) as f:
        data = json.load(f)

    all_records = data.get("instances", [])
    instances: list[AbductiveInstance] = []
    l3_count = 0

    for i, item in enumerate(all_records):
        level = item.get("level", 2)

        if level == 3:
            if not include_level3 or l3_count >= level3_limit:
                continue
            D_minus = _reconstruct_theory_from_l3(item)
            inst = AbductiveInstance(
                D_minus=D_minus,
                target=item.get("anomaly", item.get("target", "")),
                candidates=item.get("candidates", []),
                gold=[item["gold"]] if isinstance(item.get("gold"), str)
                     else item.get("gold", []),
                level=3,
                metadata={
                    "domain": item.get("domain", "rts_engagement"),
                    "scenario_id": item.get("scenario_id", f"rts-l3-{i:04d}"),
                    "nov": item.get("nov", 0.0),
                    "d_rev": item.get("d_rev", 1),
                    "conservative": item.get("conservative", True),
                },
            )
            inst.id = f"rts-l3-{item.get('scenario_id', i):}"
            l3_count += 1
        else:
            if len(instances) - l3_count >= limit:
                continue
            D_minus = _build_theory_from_record(item)
            inst = AbductiveInstance(
                D_minus=D_minus,
                target=item.get("target", ""),
                candidates=item.get("candidates", []),
                gold=item.get("gold", []),
                level=level,
                metadata={
                    "domain": "rts_engagement",
                    **item.get("metadata", {}),
                },
            )
            inst.id = item.get("metadata", {}).get("instance_id",
                                                    f"rts-l{level}-{i:04d}")

        instances.append(inst)

    return instances


def generate_rts_instances_if_needed(instances_path: Path, verbose: bool) -> bool:
    """Run generate_rts_instances.py if the output file does not exist."""
    if instances_path.exists():
        return True

    print(f"  Instance file not found: {instances_path}")
    print("  Generating RTS engagement instances...")

    import subprocess
    cmd = [
        sys.executable,
        str(ROOT / "scripts" / "generate_rts_instances.py"),
        "--output", str(instances_path),
        "--level", "0",
        "--max-instances", "15",
    ]
    result = subprocess.run(cmd, capture_output=not verbose)
    if result.returncode != 0:
        print("  ERROR: Instance generation failed.")
        if not verbose:
            print(result.stderr.decode())
        return False
    return True


def load_existing_domain_results(results_dir: Path) -> dict:
    """
    Load existing per-domain evaluation results for cross-domain comparison.
    Returns {domain: {model: {level: accuracy}}} if available.
    """
    comparison = {}
    for domain in ["biology", "legal", "materials"]:
        for f in results_dir.glob(f"*{domain}*.json"):
            try:
                with open(f) as fh:
                    data = json.load(fh)
                if "summary" in data:
                    comparison[domain] = data["summary"]
                    break
            except (json.JSONDecodeError, KeyError):
                continue
    return comparison


# ── Argument parsing ──────────────────────────────────────────────────────────

def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description="Evaluate models on DeFAb-ROE instances")

    p.add_argument("--provider", required=True,
                   choices=[
                       "openai", "azure", "curc", "anthropic", "google", "ollama",
                       "foundry-gpt", "foundry-kimi", "foundry-claude",
                       "foundry-deepseek", "mock",
                   ])
    p.add_argument("--api-key", default=None)
    p.add_argument("--model", default=None)
    p.add_argument("--endpoint", default=None)
    p.add_argument("--deployment", default=None)
    p.add_argument("--api-version", default="2024-08-01-preview")
    p.add_argument("--ollama-host", default="http://localhost:11434")
    p.add_argument("--curc-base-url", default="http://localhost:8000")
    p.add_argument("--foundry-base-url", default=None)

    p.add_argument("--modalities", nargs="+", default=["M4"])
    p.add_argument("--strategies", nargs="+", default=["direct"])
    p.add_argument("--instance-limit", type=int, default=50,
                   help="Max L1/L2 instances (default 50)")
    p.add_argument("--include-level3", action="store_true",
                   help="Include all 6 Level 3 seed conflicts")
    p.add_argument("--level3-limit", type=int, default=6)

    p.add_argument("--instances-file", default=None,
                   help="Path to RTS instances JSON (auto-generated if absent)")
    p.add_argument("--results-dir", default=str(ROOT / "experiments" / "results"),
                   help="Output directory")
    p.add_argument("--cache-dir", default=str(ROOT / "experiments" / "cache"))
    p.add_argument("--checkpoint", default=None)
    p.add_argument("--verbose", "-v", action="store_true")

    return p.parse_args()


# ── Main ──────────────────────────────────────────────────────────────────────

def main() -> int:
    args = parse_args()

    results_dir = Path(args.results_dir)
    results_dir.mkdir(parents=True, exist_ok=True)
    cache_dir = Path(args.cache_dir)
    cache_dir.mkdir(parents=True, exist_ok=True)

    print("=" * 70)
    print("DeFAb-ROE Evaluation Pipeline")
    print("=" * 70)
    print(f"Provider  : {args.provider}")
    print(f"Model     : {args.model or '(default)'}")
    print(f"Modalities: {args.modalities}")
    print(f"Strategies: {args.strategies}")
    print(f"Level 3   : {args.include_level3}")
    print()

    # ── Load / generate instances ─────────────────────────────────────────────
    default_instances_path = ROOT / "instances" / "rts_engagement_instances.json"
    instances_path = Path(args.instances_file) if args.instances_file else default_instances_path

    print(f"Loading RTS engagement instances from: {instances_path}")
    if not instances_path.exists():
        ok = generate_rts_instances_if_needed(instances_path, args.verbose)
        if not ok:
            return 1

    instances = load_rts_instances(
        instances_path,
        limit=args.instance_limit,
        include_level3=args.include_level3,
        level3_limit=args.level3_limit,
    )

    if not instances:
        print("ERROR: No instances loaded.")
        return 1

    by_level: dict[int, int] = {}
    for inst in instances:
        level = getattr(inst, "level", 2)
        by_level[level] = by_level.get(level, 0) + 1

    print(f"  Loaded {len(instances)} instances:")
    for level, count in sorted(by_level.items()):
        print(f"    Level {level}: {count}")

    # ── Build model interface ─────────────────────────────────────────────────
    kwargs: dict = {}
    api_key = args.api_key

    if args.provider == "azure":
        kwargs["endpoint"] = args.endpoint
        kwargs["deployment_name"] = args.deployment
        kwargs["api_version"] = args.api_version
    elif args.provider == "curc":
        kwargs["base_url"] = args.curc_base_url
        if args.model:
            kwargs["model"] = args.model
    elif args.provider == "ollama":
        kwargs["host"] = args.ollama_host
        if args.model:
            kwargs["model"] = args.model
    elif args.provider.startswith("foundry-"):
        if not api_key:
            api_key = os.getenv("FOUNDRY_API_KEY")
        if args.endpoint:
            kwargs["endpoint"] = args.endpoint
        if args.foundry_base_url:
            kwargs["base_url"] = args.foundry_base_url

    try:
        model = create_model_interface(args.provider, api_key=api_key, **kwargs)
    except Exception as e:
        print(f"ERROR: Could not create model interface: {e}")
        return 1

    # ── Run evaluation ────────────────────────────────────────────────────────
    print(f"\nRunning evaluation ({args.provider})...")
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    results_file = results_dir / f"rts_{args.provider}_{timestamp}.json"

    pipeline = EvaluationPipeline(
        instances=instances,
        models={args.provider: model},
        modalities=args.modalities,
        strategies=args.strategies,
        cache_dir=str(cache_dir),
        results_dir=str(results_dir),
    )

    results = pipeline.run(
        save_every=20,
        checkpoint_file=args.checkpoint,
    )

    # ── Save results ──────────────────────────────────────────────────────────
    output = {
        "domain": "rts_engagement",
        "provider": args.provider,
        "model": args.model or "(default)",
        "evaluation_date": datetime.now().isoformat(),
        "instance_counts": by_level,
        "modalities": args.modalities,
        "strategies": args.strategies,
        "summary": getattr(results, "summary", {}),
        "evaluations": [
            {
                "instance_id": ev.instance_id,
                "level": ev.level,
                "modality": ev.modality,
                "strategy": ev.strategy,
                "correct": ev.metrics.correct,
                "graded_score": ev.metrics.graded_score,
                "error_class": ev.metrics.error_class,
                "resolves_anomaly": ev.metrics.resolves_anomaly,
                "is_conservative": ev.metrics.conservativity,
            }
            for ev in getattr(results, "evaluations", [])
        ],
    }

    with open(results_file, "w") as f:
        json.dump(output, f, indent=2)
    print(f"\nResults saved: {results_file}")

    # ── Cross-domain comparison ───────────────────────────────────────────────
    existing = load_existing_domain_results(results_dir)
    if existing:
        print("\n" + "=" * 70)
        print("CROSS-DOMAIN ACCURACY COMPARISON")
        print("=" * 70)
        print(f"  {'Domain':<16} {'L2 Acc':>8} {'L3 Acc':>8} {'L3 Graded':>10}")
        print(f"  {'-'*16} {'-'*8} {'-'*8} {'-'*10}")
        for domain, summary in sorted(existing.items()):
            l2 = summary.get("level2_accuracy", "--")
            l3 = summary.get("level3_accuracy", "--")
            l3g = summary.get("level3_graded", "--")
            l2_str = f"{l2:.1%}" if isinstance(l2, float) else str(l2)
            l3_str = f"{l3:.1%}" if isinstance(l3, float) else str(l3)
            l3g_str = f"{l3g:.3f}" if isinstance(l3g, float) else str(l3g)
            print(f"  {domain:<16} {l2_str:>8} {l3_str:>8} {l3g_str:>10}")

        rts_summary = getattr(results, "summary", {})
        l2 = rts_summary.get("level2_accuracy", "--")
        l3 = rts_summary.get("level3_accuracy", "--")
        l3g = rts_summary.get("level3_graded", "--")
        l2_str = f"{l2:.1%}" if isinstance(l2, float) else str(l2)
        l3_str = f"{l3:.1%}" if isinstance(l3, float) else str(l3)
        l3g_str = f"{l3g:.3f}" if isinstance(l3g, float) else str(l3g)
        print(f"  {'rts_engagement':<16} {l2_str:>8} {l3_str:>8} {l3g_str:>10}")
        print()

    print("=" * 70)
    print("DeFAb-ROE EVALUATION COMPLETE")
    print("=" * 70)
    return 0


if __name__ == "__main__":
    sys.exit(main())
